import math

def add(num1, num2):
   print(num1 + num2)

def subtract(num1, num2):
   print(num1 - num2)

def multiply(num1, num2):
   print(num1 * num2)

def divide(num1, num2):
   print(num1 / num2)

def pow(num1, num2):
   print(math.pow(num1, num2))

def square_root(num1, num2):
   print(math.sqrt(num1, num2))
